# Sum function
def sumIt(a:float, b:float):
    return float(a) + float(b)

# Subtraction function
def subtraction(a:float, b:float):
    return float(a) - float(b)

# Division function
def division(a:float, b:float):
    if b == 0:
        return 0
    
    return float(a) / float(b)

# Multiply function
def multiply(a:float, b:float):
    return float(a) * float(b)